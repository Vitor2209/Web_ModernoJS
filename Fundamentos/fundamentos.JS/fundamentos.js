// Uma sentença de código é uma instrução completa que o JavaScript executa.
// Aqui, o console.log é uma função que imprime algo na tela (no console do navegador ou terminal).
console.log('Hi guys'); // Sentença de código

// As chaves { } delimitam um bloco de código.
// Um bloco é um agrupamento de sentenças que o JavaScript pode executar juntas.
// Ele pode estar dentro de funções, condicionais (if), loops, etc.

{
    {
        {
            {
                // Tudo entre as chaves é considerado um bloco.
                // Você pode aninhar (colocar um dentro do outro) quantos blocos quiser.
                // Isso ajuda a organizar o código.

                console.log("Bloco de codigo 02"); // Sentença dentro de um bloco
                console.log("Bloco de codigo");    // Outra sentença dentro do mesmo bloco
            }
        }
    }
}

// Importante:
// - O JavaScript ignora a indentação (os espaços no começo da linha), mas usamos ela para deixar o código legível.
// - As chaves { } são usadas para definir blocos, e o JavaScript entende que tudo dentro delas faz parte do mesmo grupo.
// - Mesmo que esses blocos não tenham função prática aqui, o objetivo é mostrar a estrutura básica da linguagem.

